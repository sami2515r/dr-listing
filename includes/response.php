<?php

/*
|--------------------------------------------------------------------------
| JSON HEADER
|--------------------------------------------------------------------------
*/

header("Content-Type: application/json");

/*
|--------------------------------------------------------------------------
| SUCCESS RESPONSE
|--------------------------------------------------------------------------
*/

function success($message, $data = []) {

    echo json_encode([

        "status" => true,
        "message" => $message,
        "data" => $data
    ]);

    exit;
}

/*
|--------------------------------------------------------------------------
| ERROR RESPONSE
|--------------------------------------------------------------------------
*/

function error($message, $data = []) {

    echo json_encode([

        "status" => false,
        "message" => $message,
        "data" => $data
    ]);

    exit;
}